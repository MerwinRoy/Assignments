    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        
        //Selected Row
        int selectedRow = tblDeliveryAgentManagement.getSelectedRow();
        
        //Prompts error if no row is selected
        if (selectedRow < 0) 
        {
            //show error message
            JOptionPane.showMessageDialog(null, "Please select a row from table to proceed");
            
            return;
        }
        else
        {
            //UserAccount object is selected from table row
            UserAccount user = (UserAccount)tblDeliveryAgentManagement.getValueAt(selectedRow, 1);
            
            //UpdateManagerJPanel object is created
            UpdateManagerJPanel updateManagerPanel = new UpdateManagerJPanel(container,user,"DeliveryMan");
            
            //panel object added to container
            container.add("UpdateManagerJPanel", updateManagerPanel);
            
            //cardlayout object is created
            CardLayout cardLayout = (CardLayout) container.getLayout();
            
            //next layout is loaded
            cardLayout.next(container);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        //Row is selected from table
        int selectedRow = tblDeliveryAgentManagement.getSelectedRow();
        
        //prompts error if no row is selected
        if (selectedRow < 0) 
        {
            //show MessageDialog
            JOptionPane.showMessageDialog(null, "Please select a row from table to proceed");
            
            return;
        }
        else
        {
            //UserAccount object is selected from table
            UserAccount account = (UserAccount)tblDeliveryAgentManagement.getValueAt(selectedRow, 1);
            
            
            //delete employee details from organization
            organization.getEmployeeDirectory().deleteEmployee(account.getEmployee());
            
            //delete account from organization
            organization.getUserAccountDirectory().deleteAccount(account);
          
            
            //Show message dialog
            JOptionPane.showMessageDialog(null, "User Account has been deleted successfully");
    } 
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        
        //Current container layout is removed
        container.remove(this);
        
        //Cardlayout object is created
        CardLayout cardLayout = (CardLayout) container.getLayout();
        
        //previous layout is loaded
        cardLayout.previous(container);
        
        //Components array is created to fetch and store components from container
        Component[] components = this.container.getComponents();
        
        //for loop to iterate through components
        for(Component component : components)
        {
            
            if(component instanceof SystemAdminWorkAreaJPanel)
            {
                //SystemAdminWorkAreaJPanel object created
                SystemAdminWorkAreaJPanel sawaPanel= (SystemAdminWorkAreaJPanel) component;
                
                sawaPanel.populateTree(); 
            }
        }
    }//GEN-LAST:event_btnBackActionPerformed


