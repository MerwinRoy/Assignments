    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed

        //Container layout
        container.remove(this);
        
        //component fetched into array from container
        Component[] compArray = container.getComponents();
        
        //Component object created
        Component component = compArray[compArray.length - 1];
        
        //DeliveryManWorkAreaJPanel object is created
        DeliveryManWorkAreaJPanel dmwap = (DeliveryManWorkAreaJPanel) component;
        
        dmwap.populateTable();
        
        //CardLayout object is created
        CardLayout cardLayout = (CardLayout) container.getLayout();
        
        //previous container is loaded
        cardLayout.previous(container);
    }//GEN-LAST:event_btnBackActionPerformed

