    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        //restaurantName
        String restaurantName=txtRestaurantName.getText();
        
        //address of restaurant
        String location=txtLocation.getText();
        
        if (restaurantName.equals("") || location.equals("") )
        {
            //Show message Dialog
            JOptionPane.showMessageDialog(null,"empty fields are not allowed!");
            
            return;   
        }
        
        
        company = ecoSystem.
                getCompanyDirectory().
                newCompany(restaurantName,location,Restaurant);
              
        organization=  (Organization)company.
                getRestaurantDirectory().
                newOrganization(Organization.Type.RestaurantAdmin);
        
        //Show messageDialog, prompts restaurant saved successfully
       JOptionPane.showMessageDialog(null, "Restaurant has been saved into the system successfully");
       {   
           return ; 
       }     
    }//GEN-LAST:event_btnSaveActionPerformed


