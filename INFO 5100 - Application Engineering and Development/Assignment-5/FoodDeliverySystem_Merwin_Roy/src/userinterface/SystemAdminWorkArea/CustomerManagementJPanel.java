    public void populateTable()
    {
        //table model object is created
        DefaultTableModel tableModel = (DefaultTableModel) tblCustomerManagement.getModel();
        
        //row count for model is set to 0
        tableModel.setRowCount(0);
        
        //organization object is created
        Organization organizationCopy=ecoSystem.
                getCustomerDirectory().
                findOrganization("Customer");
        
        //for loop to iterate through list of user accounts
        for(UserAccount account:organizationCopy.getUserAccountDirectory().getUserAccountList()) 
        {
            Object row[] = new Object[3];
            
            row[0] = account.getEmployee().getName();
            
            row[1] = account;
      
            //add row to table model after every iteration
            tableModel.addRow(row);
        }
    }
    
