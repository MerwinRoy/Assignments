    public void populateTbl()
    {
        //Table Model object created
        DefaultTableModel tableModel = (DefaultTableModel) tblDeliveryAgentManagement.getModel();
        
        //row count set to 0
        tableModel.setRowCount(0);
        
        //search delivery agent in organization
        Organization org=ecoSystem.getDeliveryManDirectory().
                findOrganization("DeliveryMan") ;
        
        //for loopt to iterate through user account list
        for(UserAccount user:org.getUserAccountDirectory().getUserAccountList()) 
        {
            Object row[] = new Object[3];
            
            row[0] = user.
                    getEmployee().
                    getName();
            
            row[1] = user;
           
            //add row to model after every iteration
            tableModel.addRow(row);
        
        }
    }
    
