    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        
        //CardLayout object created
        CardLayout cardLayout = (CardLayout)sequenceJPanel.getLayout();
        
        //current layout removed
        sequenceJPanel.remove(this);
        
        //previous panel is loaded
        cardLayout.previous(sequenceJPanel);
        
        //Components array created to fetch and store components from panel
        Component[] components = this.sequenceJPanel.
                getComponents();
        
        //for loop to iterate through each component
        for(Component component : components)
        {
            
            if(component instanceof RestaurantManagementJPanel)
            {
                //RestaurantManagementJPanel created
                RestaurantManagementJPanel mrPanel= (RestaurantManagementJPanel) component;
                
                mrPanel.populateTable(); 
                
            }
        }
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        
        //condition if there is no organization
        if(organization==null)
        {
            //Show messageDialog
            JOptionPane.showMessageDialog(null, "Save the Restaurant");
            
            return ; 
        }
        
        //AddManagerJPanel object created
        AddManagerJPanel addManPanel = new AddManagerJPanel(sequenceJPanel,ecoSystem,company,organization);
        
        //panel object added
        sequenceJPanel.add("AddManagerJPanel", addManPanel);
        
        //cardlayout object is created
        CardLayout cardLayout = (CardLayout) sequenceJPanel.getLayout();
        
        //next layout is set
        cardLayout.next(sequenceJPanel);
    }//GEN-LAST:event_btnAddActionPerformed

