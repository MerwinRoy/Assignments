    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
       //Fetch user name
       
       //Store username from textfield
       String name = txtUserName.getText();
       
       //Fetch Password
       char[] passwordArray = txtPassword.getPassword();
       
       //Store password from array
       String password = String.valueOf(passwordArray);
       
       //1) To check if user exists in user account directory
       UserAccount user  = system.
               getUserAccountDirectory().
               authenticateUser(name, password);
       
       Company companyMember = null;
       Organization organizationMember = null;
       
       if(user == null)
       {
           //2) Check inside each network and check each company
           for(Company company:system.getCompanyDirectory().getCompanyList())
           {
               if(user == null)
               {
                   //3) Check each organization and each of its company
                   for(Organization organization:company.getRestaurantDirectory().getOrganizationDir())
                   {
                       
                       user = organization.
                               getUserAccountDirectory().
                               authenticateUser(name,password);
                       
                       if(user!=null)
                       {
                           companyMember = company;
                           
                           organizationMember = organization;
                           
                           break;
                       }
                   }
               }
               else
               {
                   companyMember = company;
                   
                   break;
               }
               if(organizationMember!=null)
               {
                   break;
               }
           }
       }
      
       //3) Check for Customer/Users
       if(user==null)
       {
            for(Organization organization:system.getCustomerDirectory().getOrganizationDir())
            {
                user=organization.
                        getUserAccountDirectory().
                        authenticateUser(name, password);
                
                if(user!=null)
                {
                    
                    organizationMember=organization;
                    
                    break;
                    
                }
            }
            
       }
            
        //4) To check for deliveryPerson users
        if(user==null){
            for(Organization organization:system.getDeliveryManDirectory().getOrganizationDir())
                
            {
                user=organization.
                        getUserAccountDirectory().
                        authenticateUser(name, password);
                           
                if(user!=null)
                               
                {
                               
                    organizationMember = organization;
                               
                    break;
                               
                }
            }
        }
            
        if(user==null)
        {
            JOptionPane.showMessageDialog(null,"Invalid entry");
            
            return;
        }
        else
        {
            
            CardLayout cardLayout=(CardLayout)container.getLayout();
            
            container.add("workArea",user.getRole().createWorkArea(container, user, organizationMember, companyMember, system));
            
            cardLayout.next(container);
        }
        
        //Set textfield to disabled
        txtUserName.setEnabled(false);
        txtPassword.setEnabled(false);
        
        //Set button to disabled
        btnLogin.setEnabled(false);
        
        //Logout button is enabled
        btnLogout.setEnabled(true);

    }//GEN-LAST:event_btnLoginActionPerformed

    private void btnLogoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogoutActionPerformed
        
        //Logout button is disabled
        btnLogout.setEnabled(false);
        
        //Username textfield is enabled
        txtUserName.setEnabled(true);
        
        //Password textfield is enabled
        txtPassword.setEnabled(true);
        
        //Login button is enabled
        btnLogin.setEnabled(true);

        //textfields are set empty
        txtUserName.setText("");
        
        txtPassword.setText("");

        //remove all content from container panel
        container.removeAll();
        
        //Creating blank panel
        JPanel blankPanel = new JPanel();
        
        //Add blank panel to container
        container.add("blank", blankPanel);
        
        //Create a new cardlayout
        CardLayout cardLayout = (CardLayout) container.getLayout();
        
        //Next layout
        cardLayout.next(container);
        
        //Store information in database
        dB4OUtil.storeSystem(system);
    }//GEN-LAST:event_btnLogoutActionPerformed

