    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        
        //Selected row in table
        int selectedRow = tblCustomerManagement.getSelectedRow();
        
        //prompts error if no row is selected
        if (selectedRow < 0) 
        {
            //Show message Dialog
            JOptionPane.showMessageDialog(null, "Please select a row from table to proceed");
            
            return;
        }
        else
        {
            //Useraccount object is created
            UserAccount account = (UserAccount)tblCustomerManagement.getValueAt(selectedRow, 1);
            
            //UpdateCustomerJPanel object is created
            UpdateCustomerJPanel updatePanel = new UpdateCustomerJPanel(container,account,"Customers");
            
            //panel object added to container
            container.add("UpdateCustomerJPanel", updatePanel);
            
            //cardlayout is created
            CardLayout cardLayout = (CardLayout) container.getLayout();
            cardLayout.next(container);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        //selected row from table
        int selectedRow = tblCustomerManagement.getSelectedRow();
        
        //prompts error if no row is selected
        if (selectedRow < 0) 
        {
            //show messageDialog
            JOptionPane.showMessageDialog(null, "Please select a row from table to proceed!");
            
            return;
        }
        else
        {
            //Useraccount object selected from table
            UserAccount user = (UserAccount)tblCustomerManagement.getValueAt(selectedRow, 1);
            
            //delete account
            organization.
                    getUserAccountDirectory().
                    deleteAccount(user);
            
            //delete employee details from directory
            organization.
                    getEmployeeDirectory().
                    deleteEmployee(user.getEmployee());
            
            //show message dialog
            JOptionPane.showMessageDialog(null, "User Account has been deleted successfully");
        } 
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        
        //removing current container
        container.remove(this);
        
        //cardLayout is created
        CardLayout cardLayout = (CardLayout) container.getLayout();
        
        //previous layout is loaded
        cardLayout.previous(container);
        
        //Components array created to fetch and store components from panel
        Component[] components = this.container.
                getComponents();
        
        //for loop to iterate through components
        for(Component component : components)
        {
            
            if(component instanceof SystemAdminWorkAreaJPanel)
            {
                //systemAdminWorkArea panel object created
                SystemAdminWorkAreaJPanel sawaPanel= (SystemAdminWorkAreaJPanel) component;
                
                sawaPanel.populateTree(); 
            }
        }
    }//GEN-LAST:event_btnBackActionPerformed


